    [self.navigationController pushViewController:aVC animated:YES];
	[aVC release];
}

- (void)tableView:(UITableView *)tableView accessoryButtonTappedForRowWithIndexPath:(NSIndexPath *)indexPath {
	NSLog(@"accessoryButtonTappedForRowWithIndexPath, The section: %i, the row: %i.", indexPath.section, indexPath.row);
    BubblesImageViewController *aVC = [[BubblesImageViewController alloc] initWithNibName:@"BubblesImageViewController" bundle:nil];
    [self.navigationController pushViewController:aVC animated:YES];
	[aVC release];
